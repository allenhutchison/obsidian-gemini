/**
 * Auto-generates src/services/generated-help-references.ts from docs/ markdown files.
 *
 * Scans docs/guide/*.md and docs/reference/*.md, then produces:
 *   - Import statements for each doc
 *   - A helpResources Map<string, string> mapping reference paths to content
 *   - A markdown table string for injection into the help skill prompt
 *
 * Run: node scripts/generate-help-references.mjs
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.resolve(__dirname, '..');

// Directories to scan (relative to repo root)
const DOC_DIRS = ['docs/guide', 'docs/reference'];

// Files/patterns to exclude
const EXCLUDE = new Set(['docs/index.md', 'docs/changelog.md']);
const EXCLUDE_DIRS = ['docs/contributing', 'docs/superpowers'];

/**
 * Convert a filename like "getting-started" to a camelCase variable name
 * prefixed with "ref", e.g. "refGettingStarted".
 */
function toVarName(filename) {
	const base = path.basename(filename, '.md');
	const camel = base.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
	return 'ref' + camel.charAt(0).toUpperCase() + camel.slice(1);
}

/**
 * Extract the first markdown heading from file content.
 * Returns the heading text without the # prefix.
 */
function extractHeading(content) {
	const match = content.match(/^#\s+(.+)$/m);
	return match ? match[1].trim() : '';
}

// Collect all eligible doc files
const docFiles = [];

for (const dir of DOC_DIRS) {
	const absDir = path.resolve(root, dir);
	if (!fs.existsSync(absDir)) continue;

	const files = fs
		.readdirSync(absDir)
		.filter((f) => f.endsWith('.md'))
		.sort();
	for (const file of files) {
		const relPath = path.join(dir, file).replace(/\\/g, '/');
		if (EXCLUDE.has(relPath)) continue;
		if (EXCLUDE_DIRS.some((d) => relPath.startsWith(d))) continue;
		docFiles.push(relPath);
	}
}

// Check for name collisions in the flat references/ namespace
const seen = new Map();
for (const relPath of docFiles) {
	const basename = path.basename(relPath);
	if (seen.has(basename)) {
		console.error(
			`ERROR: Name collision for "${basename}" between "${seen.get(basename)}" and "${relPath}". ` +
				`Rename one of the files to avoid conflicts.`
		);
		process.exit(1);
	}
	seen.set(basename, relPath);
}

// Build generated TypeScript
const imports = [];
const mapEntries = [];
const tableRows = [];

for (const relPath of docFiles) {
	const basename = path.basename(relPath);
	const varName = toVarName(basename);
	const refPath = `references/${basename}`;
	const importPath = `../../${relPath}`;

	// Read file to extract heading for the table description
	const content = fs.readFileSync(path.resolve(root, relPath), 'utf-8');
	const heading = extractHeading(content) || basename.replace('.md', '');

	imports.push(`import ${varName} from '${importPath}';`);
	mapEntries.push(`\t['${refPath}', ${varName}],`);
	tableRows.push(`| \`${refPath}\` | ${heading} |`);
}

const tableHeader = '| Reference | Topic |\n| --------- | ----- |';
const tableMarkdown = [tableHeader, ...tableRows].join('\n');

// Escape for embedding in a JS template literal: backslashes first, then backticks and ${
const escapedTable = tableMarkdown.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');

const output = `/**
 * AUTO-GENERATED FILE — DO NOT EDIT
 *
 * Generated by scripts/generate-help-references.mjs
 * Source directories: ${DOC_DIRS.join(', ')}
 *
 * To regenerate: npm run generate-refs
 */

${imports.join('\n')}

export const helpResources = new Map<string, string>([
${mapEntries.join('\n')}
]);

export const helpReferencesTable = \`${escapedTable}\`;
`;

const outPath = path.resolve(root, 'src/services/generated-help-references.ts');
const existing = fs.existsSync(outPath) ? fs.readFileSync(outPath, 'utf-8') : '';

if (existing === output) {
	console.log('generated-help-references.ts is already up to date.');
} else {
	fs.writeFileSync(outPath, output, 'utf-8');
	console.log(`Generated ${path.relative(root, outPath)} with ${docFiles.length} references.`);
}
